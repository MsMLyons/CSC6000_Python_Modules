# Integers
# int() function
# results round down to the nearest whole number

int(5) # result --> 5
int(5.4) # result --> 5
int(5.8) # result --> 5
int(33/4) # result --> 8
int(2**10000) # result is a huge number

#Reals
# float() function
# results give decimals

float(5) # result --> 5.0
float(33/4) # result --> 8.25
float(33//4) # result --> 8.0
float(1/3) # result --> 0.3333333333333333
float(10000/3) # result --> 3333.3333333333335
float((1/3)*10000) # result --> 3333.333333333333

